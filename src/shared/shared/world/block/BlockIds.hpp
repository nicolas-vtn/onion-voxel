	class BlockIds
	{
		// ----- Static Methods -----
	  public:
		static int GetBlockIdCount() { return static_cast<int>(BlockId::Count); }

		static void RegisterBlockIdName(BlockId id, const std::string& name)
		{
			auto index = static_cast<size_t>(id);
			assert(index < static_cast<size_t>(BlockId::Count));

			s_BlockIdToNameMap[index] = name;
		}

		static const std::string& GetBlockIdName(BlockId id)
		{
			auto index = static_cast<size_t>(id);
			assert(index < static_cast<size_t>(BlockId::Count));

			return s_BlockIdToNameMap[index];
		}

		// ----- Private Static Members -----
	  private:
		static inline std::array<std::string, static_cast<int>(BlockId::Count)> s_BlockIdToNameMap = []
		{
			std::array<std::string, static_cast<int>(BlockId::Count)> arr{};
			arr.fill("unknown");
			return arr;
		}();
	};

